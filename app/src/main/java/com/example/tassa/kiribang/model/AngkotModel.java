public class AngkotModel {

    String angkot;
    String routeAngkot;

    String descAngkot;

    public AngkotModel() {
    }

    public String getAngkot() {
        return angkot;
    }

    public void setAngkot(String angkot) {
        this.angkot = angkot;
    }



    public String getRouteAngkot() {
        return routeAngkot;
    }

    public void setRouteAngkot(String routeAngkot) {
        this.routeAngkot = routeAngkot;
    }

    public String getDescAngkot() {
        return descAngkot;
    }

    public void setDescAngkot(String descAngkot) {
        this.descAngkot = descAngkot;
    }
}
