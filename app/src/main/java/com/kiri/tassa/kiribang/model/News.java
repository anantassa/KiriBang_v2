public class News {
    String name;
    String routeNews;
    String news;


    public News() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRouteNews() {
        return routeNews;
    }

    public void setRouteNews(String routeNews) {
        this.routeNews = routeNews;
    }

    public String getNews() {
        return news;
    }

    public void setNews(String news) {
        this.news = news;
    }
}